package control_statements;

public class Switch {

	public static void main(String[] args) {
		int i =100;
		switch(i) {
		case 10:
		System.out.println("value is " +i);
		break;
		case 20:
			System.out.println("value is "+i);
			break;
		case 30:
			System.out.println("value is "+i);
			break;
		case 40:
			System.out.println("value is "+i);
			break;
		case 50:
			System.out.println("value is "+i);
			break;
		case 60:
			System.out.println("value is "+i);
			break;
		case 70:
			System.out.println("value is "+i);
			break;
		case 80:
			System.out.println("value is "+i);
			break;
		case 90:
			System.out.println("value is "+i);
			break;
		case 100:
				System.out.println("value is "+i);
				break;
		default:
			System.out.println("default value is"+i);
		}
		

	}

}
